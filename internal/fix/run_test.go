func initCommit(t *testing.T, dir string) {
	t.Helper()
	testutil.Write(t, dir, "README.md", "init\n")
	testutil.Git(t, dir, "add", "README.md")
	testutil.Git(t, dir, "commit", "-m", "init")
}

func TestRun_CleansStaged(t *testing.T) {
	dir := testutil.GitRepo(t)
	g := git.Runner{Dir: dir}
	initCommit(t, dir)
	testutil.Write(t, dir, "a.go", "// Generated with Claude Code\n")
	testutil.Git(t, dir, "add", "a.go")

	r, err := Run(g, scan.Options{Config: config.Default()}, Options{})
	if err != nil {
		t.Fatal(err)
	}
	if r.Applied != 1 {
		t.Fatalf("applied=%d", r.Applied)
	}
	if !strings.Contains(r.Summary, "Cleaned") {
		t.Fatal(r.Summary)
	}
}

