func TestRun_SkipsPushedCommitWithoutForce(t *testing.T) {
	dir := testutil.GitRepo(t)
	g := git.Runner{Dir: dir}
	testutil.Write(t, dir, "README.md", "x\n")
	testutil.Git(t, dir, "add", "README.md")
	testutil.Git(t, dir, "commit", "-m", "init\n\nCo-authored-by: Claude <noreply@anthropic.com>")
	testutil.Git(t, dir, "branch", "-M", "main")
	testutil.Git(t, dir, "remote", "add", "origin", dir)
	testutil.Git(t, dir, "push", "-u", "origin", "main")

	r, err := Run(g, scan.Options{Config: config.Default()}, Options{})
	if err != nil {
		t.Fatal(err)
	}
	if r.SkippedCommit == 0 {
		t.Fatal("expected skipped commit markers")
	}
}

func TestCleanCommitMsgFile(t *testing.T) {
	dir := testutil.GitRepo(t)
	path := filepath.Join(dir, "msg")
	os.WriteFile(path, []byte("feat\n\nCo-authored-by: Claude <noreply@anthropic.com>\n"), 0644)

	findings := []detect.Finding{{
		Source: detect.SourceCommitTrailer, Selected: true, LineNumber: 3,
		Match: "Co-authored-by: Claude <noreply@anthropic.com>",
	}}
	if err := CleanCommitMsgFile(path, findings); err != nil {
		t.Fatal(err)
	}
	data, _ := os.ReadFile(path)
	if strings.Contains(string(data), "Claude") {
		t.Fatalf("%s", data)
	}
}

