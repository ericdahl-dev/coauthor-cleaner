func TestScanLines_ClaudeGeneratedWith(t *testing.T) {
	input := "# Generated with Claude Code\nfunc main() {}\n"
	findings := ScanLines(input, SourceStagedDiff, "app.go", DefaultRules())

	if len(findings) != 1 {
		t.Fatalf("expected 1 finding, got %d", len(findings))
	}
	f := findings[0]
	if f.RuleName != "claude-generated-with" {
		t.Errorf("rule = %q, want claude-generated-with", f.RuleName)
	}
	if f.Match != "# Generated with Claude Code" {
		t.Errorf("match = %q", f.Match)
	}
	if f.Replacement != "" {
		t.Errorf("replacement = %q, want empty", f.Replacement)
	}
	if f.Confidence != ConfidenceHigh {
		t.Errorf("confidence = %q, want high", f.Confidence)
	}
}

