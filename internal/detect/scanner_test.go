func TestCleanText_RemovesSelectedLines(t *testing.T) {
	input := "# Generated with Claude Code\nfunc main() {}\n"
	findings := ScanLines(input, SourceStagedDiff, "app.go", DefaultRules())
	for i := range findings {
		findings[i].Selected = true
	}

	cleaned := CleanText(input, findings)
	want := "func main() {}\n"
	if cleaned != want {
		t.Errorf("cleaned = %q, want %q", cleaned, want)
	}
}

func TestCleanText_PreservesUnselected(t *testing.T) {
	input := "# Generated with Claude Code\nfunc main() {}\n"
	findings := ScanLines(input, SourceStagedDiff, "app.go", DefaultRules())
	for i := range findings {
		findings[i].Selected = false
	}

	cleaned := CleanText(input, findings)
	if cleaned != input {
		t.Errorf("unselected should preserve input")
	}
}

func TestScanLines_AllowedTrailerOverride(t *testing.T) {
	input := "Co-authored-by: Claude <noreply@anthropic.com>\n"
	findings := ScanLines(input, SourceCommitMessage, "", DefaultRules(), ScanOpts{
		AllowedTrailers: []string{"Co-authored-by: Claude"},
	})
	if len(findings) != 0 {
		t.Fatalf("expected allowed trailer to be skipped, got %d", len(findings))
	}
}

func TestStrictMode_FewerMatches(t *testing.T) {
	input := "// This code was assisted by AI\n"
	strict := ScanLines(input, SourceStagedDiff, "f.go", StrictRules())
	aggressive := ScanLines(input, SourceStagedDiff, "f.go", AggressiveRules())

	if len(strict) != 0 {
		t.Errorf("strict should not match generic AI text, got %d", len(strict))
	}
	if len(aggressive) < 1 {
		t.Errorf("aggressive should match generic AI text")
	}
}
